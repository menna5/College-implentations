    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        if (!emp_Name.getText().isEmpty() && !emp_salary.getText().isEmpty())
        {
            try {
                String name = emp_Name.getText();
                String gender;
                if (btn_male.isSelected())
                {
                    gender = "Male";
                }
                else if(btn_female.isSelected())
                {
                    gender = "Female";
                }
                else
                {
                    gender = "Prefere not to say";
                }
                String dept_name = dept.getSelectedItem().toString();
                double salary = Double.parseDouble(emp_salary.getText());
                PreparedStatement stm = con.prepareStatement("insert into employee (name, gender, salary) values (?, ?, ?)");
                stm.setString(1, name);
                stm.setString(2, gender);
                stm.setInt(3, (int) salary);
                
                stm.executeUpdate();               
            } catch (SQLException ex) {
                Logger.getLogger(RegisterationForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_addActionPerformed

