    private void fillTable() {
        try {
            dtm.setRowCount(0);
            PreparedStatement stm = con.prepareStatement("select name, salary, gender from employee");
            ResultSet rs = stm.executeQuery();
            while(rs.next())
            {
                dtm.addRow(new Object[]{rs.getString(1),rs.getInt(2),rs.getString(3)});  
            }
            tbl_show.setModel(dtm);
        } catch (SQLException ex) {
            Logger.getLogger(RegisterationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void fillcomboBox() {
        try {
            PreparedStatement stm = con.prepareStatement("select * from department");
            ResultSet rs = stm.executeQuery();
            while(rs.next())
            {
                dept.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(RegisterationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
